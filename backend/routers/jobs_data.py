def _keyword_match_score(job: dict, search_terms: list[str]) -> float:
    """计算岗位与搜索词的匹配分数。
    评分规则：
    - 标题完整包含搜索词：+50（最强信号）
    - 标题包含扩展词（如搜"总经理"匹配"总监"）：+30
    - JD中包含搜索词：+15
    - 城市匹配搜索词：+10
    - 公司名匹配：+5
    """
    title = job.get("title", "").lower()
    jd = job.get("jd_text", "").lower()
    company = job.get("company", "").lower()
    city = job.get("city", "").lower()

    score = 0
    title_matched = False

    for term in search_terms:
        term_lower = term.lower()
        # 标题完整包含搜索词（最强信号）
        if term_lower in title:
            score += 50
            title_matched = True
        # JD中包含搜索词
        elif term_lower in jd:
            score += 15
        # 城市匹配（如搜"杭州"匹配城市字段）
        elif term_lower in city:
            score += 10
        # 公司名匹配
        elif term_lower in company:
            score += 5

    # 扩展词匹配（如搜"总经理"也匹配"总监"、"负责人"等）
    for term in search_terms:
        expansion = _ARCH_EXPANSION.get(term.lower(), [])
        for exp in expansion:
            exp_lower = exp.lower()
            if exp_lower in title and term.lower() not in title:
                score += 30
                title_matched = True
                break

    # 如果标题完全没有匹配任何搜索词或扩展词，降分（避免JD匹配的不相关结果排在前面）
    if not title_matched:
        score = score // 3

    return min(score, 100)  # 上限100


def filter_jobs(jobs: list[dict], user_profile, search_city: str = "") -> list[dict]:
    """Apply hard constraints: city, salary_min >= user salary_min. 远程 always accepted.
    search_city: user's input city in search box (takes priority over profile city)."""
    salary_min = user_profile.salary_min if user_profile and user_profile.salary_min else 0
    city = search_city or (user_profile.city if user_profile and user_profile.city else "")

    filtered = []
    for job in jobs:
        job_city = job.get("city", "")
        data_source = job.get("data_source", "")
        is_real = data_source in ("autocli", "boss", "himalayas", "remotive", "liepin", "51job", "zhaopin")

        # 真实数据源不严格过滤城市（搜索关键词已包含城市信息）
        if city and not is_real:
            city_match = (
                job_city == "远程"
                or job_city == city
                or city in job_city
                or job_city in city
            )
            if not city_match:
                continue

        min_salary = parse_salary_min(job.get("salary", ""))
        if min_salary == 0:
            filtered.append(job)
            continue
        if salary_min == 0 or min_salary >= salary_min:
            filtered.append(job)
    return filtered


def validate_jd_urls(jobs: list[dict]) -> list[dict]:
    """Validate jd_url accessibility via HEAD request. Marks unreachable URLs."""
    if not jobs:
        return jobs

    valid_jobs = []
    invalid_jobs = []

    with httpx.Client(timeout=5.0, follow_redirects=True) as client:
        for job in jobs:
            url = job.get("jd_url", "")
            if not url or not url.startswith("http"):
                job["_url_valid"] = False
                job["_url_status"] = "no_url"
                valid_jobs.append(job)
                continue
            try:
                resp = client.head(url)
                if resp.status_code < 400:
                    job["_url_valid"] = True
                    job["_url_status"] = str(resp.status_code)
                    valid_jobs.append(job)
                else:
                    job["_url_valid"] = False
                    job["_url_status"] = f"HTTP {resp.status_code}"
                    invalid_jobs.append(job)
            except Exception as e:
                job["_url_valid"] = False
                job["_url_status"] = "unreachable"
                invalid_jobs.append(job)

    return valid_jobs + invalid_jobs
