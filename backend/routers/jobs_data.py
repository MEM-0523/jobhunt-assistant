def parse_salary_min(salary_str: str) -> int:
    """Extract minimum salary in K from salary string like '25-40K', '30-50K·14薪'"""
    match = re.search(r'(\d+)', salary_str)
    if match:
        return int(match.group(1))
    return 0

def _tokenize(text: str) -> list[str]:
    """分词：先用jieba中文分词，再过滤过短的词"""
    # 先按特殊字符分割
    for ch in "（）()/·,:：，":
        text = text.replace(ch, " ")
    tokens = []
    for word in text.split():
        word = word.strip()
        if not word:
            continue
        # 对中文部分用jieba分词
        sub_words = list(jieba.cut(word))
        for sw in sub_words:
            sw = sw.strip()
            if len(sw) >= 2:
                tokens.append(sw)
        # 原始词也保留（防止jieba过度拆分）
        if len(word) >= 2 and word not in tokens:
            tokens.append(word)
    return tokens


